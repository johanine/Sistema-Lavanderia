    public void Insertar() throws ClassNotFoundException, SQLException
    {
        oUsuario.DNI=txtDNI.getText();
        oUsuario.Nombres=txtNombres.getText();
        oUsuario.Apellidos=txtApePaterno.getText() +" "+ txtApeMaterno.getText();
        oUsuario.Telefono=txtTelefono.getText();
        oUsuario.Direccion=txtDireccion.getText();
        oUsuario.Cargo=cboCargo.getToolTipText();
        oUsuario.Correo=txtCorreo.getText();
        oUsuario.Usuario=txtIDUsuario.getText();
        oUsuario.Contrasenia=txtContrasenia.getText();
        oUsuario.insertar();
        
    }
