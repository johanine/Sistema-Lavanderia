    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        //limpia la tabla de residuos
        while(0!=model.getRowCount()){
            model.removeRow(0);
        }
        
        ResultSet r=null;
        if(txtDesc.getText()!=""){
            r=prenda.Buscar(cbCampo.getSelectedItem().toString(), txtDesc.getText());
        }
        ArrayList <Object[]> a=new ArrayList<>();
        try {
            while(r.next()){
                //se a√±ade directamente la prenda a el dgv
                Object []b={r.getString(0),r.getString(1),r.getString(2)};
                model.addRow(b);
            }
            if(tPrenda.getRowCount()==0){
                JOptionPane.showMessageDialog(null, "No encontrado");
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmBuscarPrenda.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnAceptarActionPerformed

